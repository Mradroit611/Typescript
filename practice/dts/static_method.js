"use strict";
class Mathoperation {
    static PI = Math.PI;
    static add(num1, num2) {
        return num1 + num2;
    }
    static subs(num1, num2) {
        return num1 - num2;
    }
}
console.log(Mathoperation.PI);
console.log(Mathoperation.add(5, 10));
console.log(Mathoperation.subs(10, 5));
